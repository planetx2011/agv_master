package com.hogun.agv.model;

public class RfidInfo {
	private Integer rfidNo;
	
	private Integer rfidX;
	
	private Integer rfidY;

	public Integer getRfidNo() {
		return rfidNo;
	}

	public void setRfidNo(Integer rfidNo) {
		this.rfidNo = rfidNo;
	}

	public Integer getRfidX() {
		return rfidX;
	}

	public void setRfidX(Integer rfidX) {
		this.rfidX = rfidX;
	}

	public Integer getRfidY() {
		return rfidY;
	}

	public void setRfidY(Integer rfidY) {
		this.rfidY = rfidY;
	}
}
